class sebelas {
	public static void main(String[] args) {
		
		{
			System.out.println("menggambar sebuah persegi menggunakan karakter #");
			for(int i=1;i<=4;i++)
			{
				if (i%2==0) {
					System.out.print(" ");
				}
				for(int j=1;j<=4;j++) 
				{
					System.out.print("#");
				}
			System.out.println(" ");
			}
		}
	}
	
}